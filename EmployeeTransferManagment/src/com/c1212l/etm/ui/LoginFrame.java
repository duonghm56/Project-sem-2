    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        // TODO add your handling code here:
        //     int count =0;
        //        try{
            //            vctListAdmin = loginDAO.getAdmin();
            //            vctListEmployee = loginDAO.getEmployee();
            if (txtEmail.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please enter all field");
                return;
            }
            if (txtPassword.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please enter all field");
                return;
            }
            try {
                loginAdmin = loginBUS.getAdmin(txtEmail.getText(),new String(txtPassword.getPassword()));
                loginEmpl=loginBUS.getEmployee(txtEmail.getText(),new String(txtPassword.getPassword()));
                if (loginAdmin.getEmail() != null) {
                    this.hide();
                    email = loginAdmin.getEmail();
                    role = loginAdmin.getRole();
                    AdminUI adminUI = new AdminUI();
                    adminUI.show();
                }
                else if(loginEmpl.getEmail()!=null){
                    this.hide();
                    email = loginEmpl.getEmail();
                    role = loginEmpl.getRole();
                    password = loginEmpl.getPassword();
                    ClientUI1 clientUI = new ClientUI1();
                    clientUI.show();
                }
                else{
                    lblError.setText("Login fail");
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

    }//GEN-LAST:event_btnLoginActionPerformed

