    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            if (txtEmail.getText().equals("")) {
                lblError.setText("Please enter email");
                return;
            }
            if (txtPassword.getPassword().equals("")) {
                lblError.setText("Please enter password");
                return;
            }
            LoginBUS loginBUS = new LoginBUS();
            Admin admin = loginBUS.getAdmin(txtEmail.getText(), new String(txtPassword.getPassword()));
            if(admin != null){
                AdminUI adminUI = new AdminUI();
                adminUI.setAdmin(admin);
                adminUI.setVisible(true);
                this.setVisible(false);
            }else{                
                Employee employee = loginBUS.getEmployee(txtEmail.getText(), new String(txtPassword.getPassword()));                
                if(employee != null){                    
                    ClientUI clientUI = new ClientUI();                    
                    clientUI.setEmployee(employee);
                    clientUI.setVisible(true);
                    this.setVisible(false);
                }else{
                    lblError.setText("Invalid Email and Password");
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

