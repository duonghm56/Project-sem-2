    public void loadEmployee(Employee objEmpl) {
        employeeID = objEmpl.getEmployeeID();
        txtEmployeeNumber.setText(String.valueOf(objEmpl.getEmployeeNumber()));
        txtFullName.setText(objEmpl.getEmployeeName());
        txtRole.setText(objEmpl.getEmployeeRole());
        txtWExp.setText(String.valueOf(objEmpl.getWorkExperience()));
        txaAddress.setText(objEmpl.getAddress());
        txtEmail.setText(objEmpl.getEmail());
        jDateBirthday.setDate(objEmpl.getBirthday());
        if (objEmpl.getGender() == 1) {
            cbGender.setSelectedIndex(1);
        } else if (objEmpl.getGender() == 2) {
            cbGender.setSelectedIndex(2);
        }
        txtAllowance.setText(String.valueOf(objEmpl.getAllowance()));
        cbLocation.setSelectedIndex(objEmpl.getLocationID());
        cbDepartment.setSelectedIndex(objEmpl.getDepartmentID());
        cbProject.setSelectedIndex(objEmpl.getProjectID());
    }

    public void updateEmployee() {
        try {
            objEmpl = new Employee();
            objEmpl.setEmployeeNumber(Integer.parseInt(txtEmployeeNumber.getText()));
            objEmpl.setEmployeeName(txtFullName.getText());
            objEmpl.setEmail(txtEmail.getText());
            objEmpl.setPassword(String.valueOf(password.getPassword()));
            objEmpl.setEmployeeRole(txtRole.getText());
            if (validateInfo.checkNumber(txtWExp.getText())) {
                objEmpl.setWorkExperience(Integer.parseInt(txtWExp.getText()));
            }
            objEmpl.setAddress(txaAddress.getText());
            objEmpl.setBirthday(jDateBirthday.getDate());
            objEmpl.setGender(cbGender.getSelectedIndex());
            if (validateInfo.checkNumber(txtAllowance.getText())) {
                objEmpl.setAllowance(Float.parseFloat(txtAllowance.getText()));
            }
            objEmpl.setLocationID(LocationDAO.getLocationID((String) cbLocation.getSelectedItem()));
            objEmpl.setDepartmentID(DepartmentDAO.getDepartmentID((String) cbDepartment.getSelectedItem()));
            objEmpl.setProjectID(ProjectDAO.getProjectID((String) cbProject.getSelectedItem()));
            EmployeeDAO.updateEmployee(objEmpl, employeeID);
        } catch (SQLException ex) {
        }
    }

    public boolean validateSelect() {
        if (cbDepartment.getSelectedIndex() == 0 || cbLocation.getSelectedIndex() == 0 || cbProject.getSelectedIndex() == 0 || cbGender.getSelectedIndex() == 0) {
            return false;
        } else {
            return true;
        }
    }
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (!validateInfo.checkNumber(txtEmployeeNumber.getText())) {
            lblMsg.setText("Please enter Employee number");
            txtEmployeeNumber.requestFocus();
            txtEmployeeNumber.requestFocus();
        } else if (!validateInfo.check(txtFullName.getText())) {
            lblMsg.setText("Please enter Name");
            txtFullName.requestFocus();
        } else if (!validateInfo.check(txtEmail.getText())) {
            lblMsg.setText("Please enter Email");
            txtEmail.requestFocus();
        } else if (!validateInfo.checkEmail(txtEmail.getText())) {
            lblMsg.setText("Wrong format Email");
            txtEmail.requestFocus();
        } else if (!validateInfo.check(password.getText())) {
            lblMsg.setText("Please enter Password");
            password.requestFocus();
        } else if (!validateInfo.check(txaAddress.getText())) {
            lblMsg.setText("Please enter Address");
            txaAddress.requestFocus();
        } else if (jDateBirthday.getDate() == null) {
            lblMsg.setText("Please enter birthdate");
        } else if (cbLocation.getSelectedIndex() == 0) {
            lblMsg.setText("Please select Location");
        } else if (cbDepartment.getSelectedIndex() == 0) {
            lblMsg.setText("Please select Department");
        } else if (cbProject.getSelectedIndex() == 0) {
            lblMsg.setText("Please select Project");
        } else {
            lblMsg.setText(null);
            updateEmployee();
            MainAdministrator.onLoadTableEmployee();
            JOptionPane.showMessageDialog(null, "Update Employee success");
            this.dispose();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

