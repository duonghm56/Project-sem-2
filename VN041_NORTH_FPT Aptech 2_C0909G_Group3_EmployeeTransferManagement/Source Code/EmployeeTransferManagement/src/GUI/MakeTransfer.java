    public void insertTransfer() {
        try {
            objTransfer = new Transfer();
            int transferTypeID = 1;
            objTransfer.setTransferTypeID(transferTypeID);
            objTransfer.setEmployeeID(employeeID);
            objTransfer.setFromProjectID(ProjectDAO.getProjectID(lblFromProjectContent.getText()));
            objTransfer.setFromLocationID(LocationDAO.getLocationID(txaFromLocation.getText()));
            objTransfer.setFromDepartmentID(DepartmentDAO.getDepartmentID(lblFromDepartmentContent.getText()));
            objTransfer.setToProjectID(ProjectDAO.getProjectID((String) cbToProject.getSelectedItem()));
            objTransfer.setToLocationID(LocationDAO.getLocationID((String) cbToLocation.getSelectedItem()));
            objTransfer.setToDepartmentID(DepartmentDAO.getDepartmentID((String) cbToDepartment.getSelectedItem()));
            objTransfer.setTransferRelievingDate(jDateRelieving.getDate());
            objTransfer.setTransferJoiningDate(jDateJoining.getDate());
            Date requestDate = new Date();
            Date approveDate = new Date();
            objTransfer.setRequestDate(requestDate);
            objTransfer.setApprove("Approve");
            objTransfer.setApproveDate(approveDate);
            int compareDate = jDateRelieving.getDate().compareTo(jDateJoining.getDate());
            if (compareDate > 0) {
                lblMsg.setText("Joining date is greater than relieving date");
            } else {
                lblMsg.setText(null);
                TransferDAO.insertTransfer(objTransfer);
            }

        } catch (SQLException ex) {
            Logger.getLogger(MakeTransfer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

