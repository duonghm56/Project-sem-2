    private void btnMakeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMakeActionPerformed
        // TODO add your handling code here:
        if (cbEmployee.getSelectedIndex() == 0 || cbToLocation.getSelectedIndex() == 0 || cbToDepartment.getSelectedIndex() == 0 || jDateJoining.getDate() == null || jDateRelieving.getDate() == null) {
            lblMsg.setText("Please enter or select information");
        } else {
            insertTransfer();
            MainAdministrator.onLoadTableTransfer();
            JOptionPane.showMessageDialog(null, "Make transfer employee success");
        }
    }//GEN-LAST:event_btnMakeActionPerformed

    private void cbEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbEmployeeActionPerformed
        try {
            // TODO add your handling code here:
            if (cbEmployee.getSelectedIndex() == 0) {
                txaFromLocation.setText(null);
                lblFromDepartmentContent.setText(null);
                lblFromProjectContent.setText(null);
            } else {
                String employeeName = (String) cbEmployee.getSelectedItem();
                int departmentID = EmployeeDAO.getEmployee(employeeName).getDepartmentID();
                int locationID = EmployeeDAO.getEmployee(employeeName).getLocationID();
                int projectID = EmployeeDAO.getEmployee(employeeName).getProjectID();

                employeeID = EmployeeDAO.getEmployeeID((String) cbEmployee.getSelectedItem());
                txaFromLocation.setText(LocationDAO.getLocationName(locationID));
                lblFromDepartmentContent.setText(DepartmentDAO.getDepartmentName(departmentID));
                lblFromProjectContent.setText(ProjectDAO.getProjectName(projectID));
            }
        } catch (SQLException ex) {
            Logger.getLogger(MakeTransfer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cbEmployeeActionPerformed

