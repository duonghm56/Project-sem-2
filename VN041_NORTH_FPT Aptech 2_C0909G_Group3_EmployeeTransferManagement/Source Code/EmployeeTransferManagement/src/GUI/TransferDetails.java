    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        if (dateRelieving.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Please enter date relieving");
        } else if (dateJoining.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Please enter date joining");
        } else {
            objTransfer = new Transfer();
            Date relievingDate = dateRelieving.getDate();
            Date joiningDate = dateJoining.getDate();
            Date approveDate = new Date();
            objTransfer.setTransferID(transferID);
            objTransfer.setTransferRelievingDate(relievingDate);
            objTransfer.setTransferJoiningDate(joiningDate);
            objTransfer.setApprove("Approve");
            objTransfer.setApproveDate(approveDate);

            TransferDAO.approveTransfer(objTransfer);
            JOptionPane.showMessageDialog(null, "Approve success!");
            MainAdministrator.onLoadTableTransfer();
            this.dispose();
            btnApprove.setEnabled(false);
            btnNotApprove.setEnabled(false);
        }

    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnNotApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNotApproveActionPerformed
        // TODO add your handling code here:
        objTransfer = new Transfer();
        objTransfer.setTransferID(transferID);
        objTransfer.setApprove("Not Approve");

        TransferDAO.notApproveTransfer(objTransfer);
        JOptionPane.showMessageDialog(null, "Not Approve success!");
        MainAdministrator.onLoadTableTransfer();
        this.dispose();
        btnApprove.setEnabled(false);
        btnNotApprove.setEnabled(false);

    }//GEN-LAST:event_btnNotApproveActionPerformed

