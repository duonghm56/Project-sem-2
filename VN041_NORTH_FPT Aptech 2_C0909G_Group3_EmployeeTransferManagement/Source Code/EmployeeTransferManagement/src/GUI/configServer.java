    public void saveServerConfig() throws Exception {
        FileOutputStream fos = new FileOutputStream("config.properties");
        Properties properties = new Properties();
        databaseName = (String) properties.setProperty("databaseName", txtDatabasename.getText());
        serverName = (String) properties.setProperty("serverName", txtServername.getText());
        portNumber = (String) properties.setProperty("portNumber", txtPort.getText());
        Username = (String) properties.setProperty("Username", txtUsername.getText());
        Password = (String) properties.setProperty("Password", String.valueOf(txtPassword.getPassword()));
        properties.store(fos, null);
        fos.close();
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            if (!validateInfo.check(txtServername.getText()) ||
                    !validateInfo.checkNumber(txtPort.getText()) ||
                    !validateInfo.check(txtDatabasename.getText()) ||
                    !validateInfo.check(String.valueOf(txtPassword.getPassword()))) {
                lblMsg.setText("Invalid Information");
            } else {
                lblMsg.setText(null);
                saveServerConfig();
                JOptionPane.showMessageDialog(null, "Config server success !", "Messages",JOptionPane.PLAIN_MESSAGE, new ImageIcon("src\\Icons\\24x24\\warning.png"));
                LoginSystem dialog = new LoginSystem(this, true);
                dialog.setLocationRelativeTo(dialog);
                this.dispose();
                dialog.setVisible(true);
            }
        } catch (Exception ex) {
            Logger.getLogger(configServer.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_btnSaveActionPerformed

